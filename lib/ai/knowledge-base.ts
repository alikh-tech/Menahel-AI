// Common Hebrew prefixes (definite article + one/two-letter prepositions) that
// get glued onto the following word (e.g. "ב-מערכת" -> "במערכת", "ה-ציונים" ->
// "הציונים"). Stripping them lets "שכר הלימוד" match "לשכר לימוד", etc.
const HEBREW_PREFIX_RE = /^[בכלמהוש]{1,2}(?=.{2,})/;

// Characters to drop entirely before tokenizing (punctuation, quotes, etc.)
const PUNCTUATION_RE = /["'׳״?!.,;:()‘’“”]/g;

/** Lowercase, strip punctuation and collapse whitespace. */
function normalize(text: string): string {
  return text
    .toLowerCase()
    .replace(PUNCTUATION_RE, " ")
    .replace(/\s+/g, " ")
    .trim();
}

/** Split into normalized word tokens, stripping common Hebrew prefix letters. */
function tokenize(text: string): string[] {
  return normalize(text)
    .split(" ")
    .filter(Boolean)
    .map((word) => word.replace(HEBREW_PREFIX_RE, ""));
}

interface ScoredEntry {
  entry: FAQEntry;
  score: number;
}

/** Minimum score required for a knowledge-base entry to be considered a match. */
const MATCH_THRESHOLD = 2;

/**
 * Searches the knowledge base for the entry that best matches `message`.
 *
 * Matching strategy (highest to lowest weight):
 * 1. Exact match - the normalized message equals the entry's normalized question.
 * 2. Substring match - the normalized message contains the question (or vice
 *    versa), e.g. "תוכל בבקשה לתת לי אישור לימודים?" contains "אישור לימודים".
 * 3. Keyword match - any of the entry's `keywords` appears as a substring of
 *    the message (case-insensitive, punctuation-insensitive).
 * 4. Partial / token overlap - counts shared word tokens (with Hebrew prefix
 *    stripping) between the message and the entry's question + keywords, so
 *    questions phrased differently from the canonical form can still match.
 *
 * Returns `null` if no entry reaches `MATCH_THRESHOLD`.
 */
export function findFAQAnswer(message: string): FAQEntry | null {
  const normalizedMessage = normalize(message);
  if (!normalizedMessage) return null;

  const messageTokens = new Set(tokenize(message));

  let best: ScoredEntry | null = null;

  for (const entry of KNOWLEDGE_BASE) {
    const normalizedQuestion = normalize(entry.question);
    let score = 0;

    // 1. Exact match
    if (normalizedMessage === normalizedQuestion) {
      score += 100;
    }

    // 2. Substring match (either direction)
    if (
      normalizedQuestion.length > 0 &&
      (normalizedMessage.includes(normalizedQuestion) ||
        normalizedQuestion.includes(normalizedMessage))
    ) {
      score += 10;
    }

    // 3. Keyword match
    for (const keyword of entry.keywords) {
      const normalizedKeyword = normalize(keyword);
      if (normalizedKeyword && normalizedMessage.includes(normalizedKeyword)) {
        score += 5;
      }
    }

    // 4. Partial / token overlap between the message and question + keywords
    const referenceTokens = tokenize(
      [entry.question, ...entry.keywords].join(" ")
    );
    let overlap = 0;
    for (const token of referenceTokens) {
      if (token.length > 1 && messageTokens.has(token)) {
        overlap += 1;
      }
    }
    score += overlap;

    if (score > 0 && (!best || score > best.score)) {
      best = { entry, score };
    }
  }

  if (best && best.score >= MATCH_THRESHOLD) {
    return best.entry;
  }

  return null;
}
