import type { DemoAction } from "@/types";

export interface DemoResponse {
  content: string;
  actions?: DemoAction[];
}

const DOCUMENT_ACTIONS: DemoAction[] = [
  { label: "הצג", type: "view" },
  { label: "הורד", type: "download" },
  { label: "שלח למייל", type: "email" },
];

const DEFAULT_RESPONSE: DemoResponse = {
  content:
    "אני פועל כרגע במצב הדגמה (Demo Mode), ועדיין יכול לעזור! אפשר לשאול אותי על אישור לימודים, גליון ציונים, מערכת שעות, כרטיס נבחן, בקשות למועד מיוחד, ממוצע ציונים, נקודות זכות, חוב שכר לימוד, מועדי מבחנים או סיכום המצב האקדמי שלך.",
};

export function getDemoResponse(message: string): DemoResponse {
  const text = message.trim();

  if (text.includes("אישור לימודים")) {
    return {
      content:
        'הנה אישור הלימודים שלך לשנת הלימודים תשפ"ו, סמסטר ב׳. המסמך כולל את פרטי הרישום, מסלול הלימודים, היקף השעות וסטטוס הרשמה פעיל. אפשר לצפות בו, להוריד אותו או לשלוח אותו ישירות למייל.',
      actions: DOCUMENT_ACTIONS,
    };
  }

  if (text.includes("גליון ציונים")) {
    return {
      content:
        "הנה גליון הציונים המעודכן שלך, הכולל את כל הקורסים שהושלמו עד כה עם הציונים הסופיים וצבירת נקודות הזכות בכל סמסטר.",
      actions: DOCUMENT_ACTIONS,
    };
  }

  if (text.includes("מערכת שעות")) {
    return {
      content:
        "הנה מערכת השעות שלך לסמסטר הנוכחי - כוללת את כל השיעורים, התרגולים והמעבדות לפי ימים ושעות, כולל מספרי חדרים ושמות מרצים.",
      actions: DOCUMENT_ACTIONS,
    };
  }

  if (text.includes("כרטיס נבחן")) {
    return {
      content:
        "הנה כרטיס הנבחן שלך לתקופת המבחנים הקרובה - מרכז את כל מועדי הבחינות, מספרי החדרים ומספר הנבחן שלך לכל קורס.",
      actions: DOCUMENT_ACTIONS,
    };
  }

  if (text.includes("מועד מיוחד")) {
    return {
      content:
        "הכנתי עבורך טופס בקשה למועד מיוחד. מלאו את הפרטים הנדרשים (קורס, סיבת הבקשה ואסמכתאות) ושלחו אותו לוועדת ההוראה. אפשר לצפות בטופס, להוריד אותו או לשלוח אותו למייל.",
      actions: DOCUMENT_ACTIONS,
    };
  }

  if (text.includes("ממוצע")) {
    return {
      content:
        'הממוצע המשוקלל הנוכחי שלך עומד על 87.4, על סמך כל הקורסים שהושלמו עד כה. זהו שיפור של כ-2 נקודות לעומת הסמסטר הקודם - כל הכבוד!',
    };
  }

  if (text.includes("נקודות זכות")) {
    return {
      content:
        "צברת עד כה 78 מתוך 120 נקודות הזכות הנדרשות להשלמת התואר - כלומר כ-65% מהדרישות. בקצב הנוכחי צפוי שתסיימו את התואר בעוד כ-3 סמסטרים.",
    };
  }

  if (text.includes("חוב") && text.includes("שכר לימוד")) {
    return {
      content:
        "נכון להיום קיימת יתרת חוב של 1,450 ₪ עבור שכר הלימוד של הסמסטר הנוכחי. התשלום הבא נקבע ל-1 בחודש הבא. ניתן לעקוב אחרי כל החיובים והתשלומים במרכז הכספים.",
    };
  }

  if (text.includes("מבחן") && (text.includes("קרוב") || text.includes("מתי"))) {
    return {
      content:
        'המבחן הקרוב שלך הוא במקצוע "מבני נתונים", ביום שלישי הקרוב בשעה 09:00 באולם 305. מומלץ להתחיל בחזרות מוקדם - נשארו כ-5 ימים להיערכות.',
    };
  }

  if (text.includes("סכם") && text.includes("מצב אקדמי")) {
    return {
      content:
        "סיכום המצב האקדמי שלך: ממוצע משוקלל 87.4, צברת 78 מתוך 120 נקודות זכות (כ-65% מהדרישות לתואר), קיימת יתרת חוב שכר לימוד של 1,450 ₪, והמבחן הקרוב הוא במבני נתונים ביום שלישי הקרוב. בסך הכל - מצב טוב ויציב, עם דגש לתשומת לב על תשלום שכר הלימוד והיערכות למבחן הקרוב.",
    };
  }

  return DEFAULT_RESPONSE;
}
